// Get unique values for filters
export const getUniqueValues = () => {
  const boroughs = [...new Set(planningApplications.map(app => app.borough))].sort();
  const neighbourhoods = [...new Set(planningApplications.map(app => app.neighbourhood))].sort();
  const architects = [...new Set(planningApplications.map(app => app.architect))].sort();
  const developers = [...new Set(planningApplications.map(app => app.developer))].sort();
  const projectTypes = [...new Set(planningApplications.map(app => app.project_type))].sort();
  const useClasses = [...new Set(planningApplications.map(app => app.use_class))].sort();
  const allMaterials = [...new Set(planningApplications.flatMap(app => app.materials))].sort();
  const allSustainabilityFeatures = [...new Set(planningApplications.flatMap(app => app.sustainability_features))].sort();

  return {
    boroughs,
    neighbourhoods,
    architects,
    developers,
    projectTypes,
    useClasses,
    materials: allMaterials,
    sustainabilityFeatures: allSustainabilityFeatures
  };
};
